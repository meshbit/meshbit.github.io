def expand(keyword: str, max_variants: int = 8) -> list:
    """展开关键词为变体列表，返回 [原词, 变体1, 变体2, ...]"""
    keyword = keyword.strip()
    # 精确匹配
    if keyword in EXPAND:
        variants = [keyword] + [v for v in EXPAND[keyword] if v != keyword]
        return variants[:max_variants]
    # 部分匹配（如搜"哈利"可匹配"哈利波特"）
    for main, variants in EXPAND.items():
        if main in keyword or keyword in main:
            all_v = [keyword] + [v for v in variants]
            return all_v[:max_variants]
    return [keyword]


def normalize_note(note: str) -> str:
    """标准化资源标题，用于跨源去重"""
    # 去除常见噪音前缀/后缀
    note = re.sub(r'[【\[].*?[】\]]', '', note)  # 去括号标注
    note = re.sub(r'^[\d]+[-_.\s]*', '', note)   # 去开头序号
    note = re.sub(r'[-_.\s]*$', '', note)         # 去结尾标点
    note = re.sub(r'ID[:：]\d+', '', note)        # 去 ID:xxx
    return note.strip()[:50]  # 取前50字符做特征
